#include <bits/stdc++.h>
using namespace std;

/**
 * @brief Add two integers.
 * @param a Left operand.
 * @param b Right operand.
 * @return Sum of a and b.
 */
int add(int a, int b) {
    return a + b;
}

int main() {
    cout << "Hello from user1! 2 + 3 = " << add(2, 3) << '\n';
    return 0;
}
